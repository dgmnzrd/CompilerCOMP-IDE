    @Override
    public void actionPerformed( ActionEvent e ) 
    {
        // Verificar si el progressBar ya alcanzÃ³ el 100%
        if( jpgrProgreso.getValue() >= 100 ) 
        {
            // Detener el timer y lanzar el siguiente Frame
            timer.stop ();
            new Compiler ().setVisible( true );
            dispose ();
        } 
        else 
        {
            Random inc = new Random();
            // Incrementar en 5% el progreso
            jpgrProgreso.setValue( jpgrProgreso.getValue() + 1 + inc.nextInt( 8 ) );
        }
    }

